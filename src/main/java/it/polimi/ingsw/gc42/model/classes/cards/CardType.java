package it.polimi.ingsw.gc42.model.classes.cards;

/**
 * Enumeration of all possible Types of Cards.
 * Used to determine of what Cards a Deck is made of.
 */
public enum CardType {
    /**
     * Resource type
     */
    RESOURCECARD,
    /**
     * Gold type
     */
    GOLDCARD,
    /**
     * Starter type
     */
    STARTERCARD,
    /**
     * Objective type
     */
    OBJECTIVECARD
}
