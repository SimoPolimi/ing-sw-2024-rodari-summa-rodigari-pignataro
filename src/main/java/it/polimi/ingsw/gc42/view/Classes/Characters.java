package it.polimi.ingsw.gc42.view.Classes;

/**
 * Enumeration of all possible Characters that can be printed in TUI to represent game objects
 */
public enum Characters {
    // Kingdom Icons
    /**
     * Fungi Character
     */
    FUNGI,
    /**
     * Plant Character
     */
    PLANT,
    /**
     * Animal Character
     */
    ANIMAL,
    /**
     * Insect Character
     */
    INSECT,

    // Item Icons
    /**
     * Feather Character
     */
    FEATHER,
    /**
     * Scroll Character
     */
    SCROLL,
    /**
     * Potion Character
     */
    POTION,

    // Card Texture Tiles
    /**
     * Fungi filler
     */
    RED_SQUARE,
    /**
     * Animal filler
     */
    BLUE_SQUARE,
    /**
     * Plant filler
     */
    GREEN_SQUARE,
    /**
     * Insect filler
     */
    PURPLE_SQUARE,
    /**
     * Starter filler
     */
    YELLOW_SQUARE,

    // Other uses Tiles
    /**
     * Empty tiles
     */
    EMPTY_SPACE,
    /**
     * White tiles
     */
    WHITE_SQUARE,

    // Empty Corner Tile
    /**
     * Empty corner tile
     */
    EMPTY_CORNER,

    // Tokens
    /**
     * Red token
     */
    RED_CIRCLE,
    /**
     * Blue token
     */
    BLUE_CIRCLE,
    /**
     * Green token
     */
    GREEN_CIRCLE,
    /**
     * Yellow token
     */
    YELLOW_CIRCLE,
}
